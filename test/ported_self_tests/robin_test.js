  var s_fwd_expect = [
    [ 189588.423282507836,  107318.530350702888],
    [ 189588.423282507836, -107318.530350702888],
    [-189588.423282507836,  107318.530350702888],
    [-189588.423282507836, -107318.530350702888]
  ];

