var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('nell.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
   [ 200, 100],
   [ 200,-100],
   [-200, 100],
   [-200,-100]
  ];

  describe('Nell', function () {

    var s_fwd_expect = [
    [ 223385.132504695706,  111698.23644718733],
    [ 223385.132504695706, -111698.23644718733],
    [-223385.132504695706,  111698.23644718733],
    [-223385.132504695706, -111698.23644718733]
    ];

    var s_inv_expect = [
    [ 0.00179049310989310567,  0.000895246554910125161],
    [ 0.00179049310989310567, -0.000895246554910125161],
    [-0.00179049310989310567,  0.000895246554910125161],
    [-0.00179049310989310567, -0.000895246554910125161]
    ];

    var sargs = "+proj=nell   +a=6400000    +lat_1=0.5 +lat_2=2";
    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })

});
