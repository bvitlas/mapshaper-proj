var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('aeqd.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
    [ 200, 100],
    [ 200,-100],
    [-200, 100],
    [-200,-100]
  ];

  var s_fwd_expect = [
    [ 223379.456047271,  111723.757570854126],
    [ 223379.456047271, -111723.757570854126],
    [-223379.456047271,  111723.757570854126],
    [-223379.456047271, -111723.757570854126]
  ];

  var s_inv_expect = [
    [ 0.00179049310992953335,  0.000895246554746200623],
    [ 0.00179049310992953335, -0.000895246554746200623],
    [-0.00179049310992953335,  0.000895246554746200623],
    [-0.00179049310992953335, -0.000895246554746200623]
  ];

  var e_fwd_expect = [
    [ 222616.522190051648,  110596.996549550197],
    [ 222616.522190051648, -110596.996549550211],
    [-222616.522190051648,  110596.996549550197],
    [-222616.522190051648, -110596.996549550211]
  ];

  var e_inv_expect = [
    [ 0.00179663056838724787,  0.000904369476930248902],
    [ 0.00179663056838724787, -0.000904369476930248469],
    [-0.00179663056838724787,  0.000904369476930248902],
    [-0.00179663056838724787, -0.000904369476930248469]
  ];

  var sargs = "+proj=aeqd   +a=6400000    +lat_1=0.5 +lat_2=2";
  var eargs = "+proj=aeqd   +ellps=GRS80  +lat_1=0.5 +lat_2=2";

  helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
  helpers.inv_test(sargs, inv_in, s_inv_expect);
  helpers.fwd_test(eargs, fwd_in, e_fwd_expect);
  helpers.inv_test(eargs, inv_in, e_inv_expect);
});
