var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('cass.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
    [ 200, 100],
    [ 200,-100],
    [-200, 100],
    [-200,-100]
  ];

  var s_fwd_expect = [
    [ 223368.10520348375,  111769.14504058579],
    [ 223368.10520348375, -111769.14504058579],
    [-223368.10520348375,  111769.14504058579],
    [-223368.10520348375, -111769.14504058579]
  ];

  var s_inv_expect = [
    [ 0.0017904931100023887,   0.00089524655445477922],
    [ 0.0017904931100023887,  -0.00089524655445477922],
    [-0.0017904931100023887,   0.00089524655445477922],
    [-0.0017904931100023887,  -0.00089524655445477922]
  ];

  var e_fwd_expect = [
    [ 222605.28577699114,   110642.22925399939],
    [ 222605.28577699114,  -110642.22925399939],
    [-222605.28577699114,   110642.22925399939],
    [-222605.28577699114,  -110642.22925399939]
  ];

  var e_inv_expect = [
    [ 0.0017966305684613522,   0.00090436947663183841],
    [ 0.0017966305684613522,  -0.00090436947663183841],
    [-0.0017966305684613522,   0.00090436947663183841],
    [-0.0017966305684613522,  -0.00090436947663183841]
  ];

  var sargs = "+proj=cass   +a=6400000    +lat_1=0.5 +lat_2=2";
  var eargs = "+proj=cass   +ellps=GRS80  +lat_1=0.5 +lat_2=2";

  helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
  helpers.inv_test(sargs, inv_in, s_inv_expect);
  helpers.fwd_test(eargs, fwd_in, e_fwd_expect);
  helpers.inv_test(eargs, inv_in, e_inv_expect);
});
