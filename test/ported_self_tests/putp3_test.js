var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('putp3.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];
  var inv_in = [
   [ 200, 100],
   [ 200,-100],
   [-200, 100],
   [-200,-100]
  ];
  describe('Putnins P3', function () {

    var s_fwd_expect = [
       [ 178227.115507793525,  89124.5607860879827],
       [ 178227.115507793525, -89124.5607860879827],
       [-178227.115507793525,  89124.5607860879827],
       [-178227.115507793525, -89124.5607860879827]
    ];
    var s_inv_expect = [
       [ 0.00224405032986489889,  0.00112202516475805899],
       [ 0.00224405032986489889, -0.00112202516475805899],
       [-0.00224405032986489889,  0.00112202516475805899],
       [-0.00224405032986489889, -0.00112202516475805899]
    ];
    var sargs = "+proj=putp3   +a=6400000    +lat_1=0 +lat_2=2";
    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })

  describe('Putnins P3\'', function () {

    var s_fwd_expect = [
       [ 178238.118539984745,  89124.5607860879827],
       [ 178238.118539984745, -89124.5607860879827],
       [-178238.118539984745,  89124.5607860879827],
       [-178238.118539984745, -89124.5607860879827]
    ];
    var s_inv_expect = [
       [ 0.00224405032969050844,  0.00112202516475805899],
       [ 0.00224405032969050844, -0.00112202516475805899],
       [-0.00224405032969050844,  0.00112202516475805899],
       [-0.00224405032969050844, -0.00112202516475805899]
    ];
    var sargs = "+proj=putp3p   +a=6400000    +lat_1=0 +lat_2=2";
    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })
});
