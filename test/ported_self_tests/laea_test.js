var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('laea.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
    [ 200, 100],
    [ 200,-100],
    [-200, 100],
    [-200,-100]
  ];

  var s_fwd_expect = [
    [ 223365.281370124663,  111716.668072915665],
    [ 223365.281370124663, -111716.668072915665],
    [-223365.281370124663,  111716.668072915665],
    [-223365.281370124663, -111716.668072915665]
  ];

  var s_inv_expect = [
    [ 0.00179049311002060264,  0.000895246554791735271],
    [ 0.00179049311002060264, -0.000895246554791735271],
    [-0.00179049311002060264,  0.000895246554791735271],
    [-0.00179049311002060264, -0.000895246554791735271]
  ];

  var e_fwd_expect = [
    [ 222602.471450095181,  110589.82722441027],
    [ 222602.471450095181, -110589.827224408786],
    [-222602.471450095181,  110589.82722441027],
    [-222602.471450095181, -110589.827224408786]
  ];

  var e_inv_expect = [
    [ 0.00179663056847900867,  0.000904369475966495845],
    [ 0.00179663056847900867, -0.000904369475966495845],
    [-0.00179663056847900867,  0.000904369475966495845],
    [-0.00179663056847900867, -0.000904369475966495845]
  ];

  var sargs = "+proj=laea   +a=6400000    +lat_1=0.5 +lat_2=2";
  var eargs = "+proj=laea   +ellps=GRS80  +lat_1=0.5 +lat_2=2";

  helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
  helpers.inv_test(sargs, inv_in, s_inv_expect);
  helpers.fwd_test(eargs, fwd_in, e_fwd_expect);
  helpers.inv_test(eargs, inv_in, e_inv_expect);
});
