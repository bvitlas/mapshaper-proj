var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('putp5.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];
  var inv_in = [
   [ 200, 100],
   [ 200,-100],
   [-200, 100],
   [-200,-100]
  ];
  describe('Putnins P6', function () {

    var s_fwd_expect = [
       [ 226369.395133402577,  110218.523796520662],
       [ 226369.395133402577, -110218.523796520749],
       [-226369.395133402577,  110218.523796520662],
       [-226369.395133402577, -110218.523796520749]
    ];
    var s_inv_expect = [
       [ 0.00176671315102969921,  0.000907295534210503544],
       [ 0.00176671315102969921, -0.000907295534205924308],
       [-0.00176671315102969921,  0.000907295534210503544],
       [-0.00176671315102969921, -0.000907295534205924308]
    ];
    var sargs = "+proj=putp6   +a=6400000    +lat_1=0 +lat_2=2";
    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })

  describe('Putnins P6\'', function () {

    var s_fwd_expect = [
       [ 198034.195132195076,  125989.475461323193],
       [ 198034.195132195076, -125989.475461323193],
       [-198034.195132195076,  125989.475461323193],
       [-198034.195132195076, -125989.475461323193]
    ];
    var s_inv_expect = [
       [ 0.00201955053120177067,  0.000793716441164738612],
       [ 0.00201955053120177067, -0.000793716441164738612],
       [-0.00201955053120177067,  0.000793716441164738612],
       [-0.00201955053120177067, -0.000793716441164738612]
    ];
    var sargs = "+proj=putp6p   +a=6400000    +lat_1=0 +lat_2=2";
    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })
});
