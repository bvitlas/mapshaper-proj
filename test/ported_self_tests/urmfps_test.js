var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('urmfps.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
    [ 200, 100],
    [ 200,-100],
    [-200, 100],
    [-200,-100]
  ];

  describe('Urmaev Flat-Polar Sinusoidal', function () {
    var s_fwd_expect = [
     [ 196001.70813419219,  127306.84332999329],
     [ 196001.70813419219, -127306.84332999329],
     [-196001.70813419219,  127306.84332999329],
     [-196001.70813419219, -127306.84332999329]
    ];

    var s_inv_expect = [
     [ 0.002040720839642371,  0.00078547381740438178],
     [ 0.002040720839642371, -0.00078547381740438178],
     [-0.002040720839642371,  0.00078547381740438178],
     [-0.002040720839642371, -0.00078547381740438178]
    ];

    var sargs = "+proj=urmfps   +a=6400000    +lat_1=0.5 +lat_2=2 +n=0.5";

    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })

  describe('UWagner I (Kavraisky VI)', function () {
    var s_fwd_expect = [
     [ 195986.78156115755,  127310.07506065986],
     [ 195986.78156115755, -127310.07506065986],
     [-195986.78156115755,  127310.07506065986],
     [-195986.78156115755, -127310.07506065986]
    ];

    var s_inv_expect = [
     [ 0.002040720839738254,  0.00078547381739207999],
     [ 0.002040720839738254, -0.00078547381739207999],
     [-0.002040720839738254,  0.00078547381739207999],
     [-0.002040720839738254, -0.00078547381739207999]
    ];

    var sargs = "+proj=wag1   +a=6400000    +lat_1=0.5 +lat_2=2 +n=0.5";

    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })

});
