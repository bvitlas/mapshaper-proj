var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('putp5.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];
  var inv_in = [
   [ 200, 100],
   [ 200,-100],
   [-200, 100],
   [-200,-100]
  ];
  describe('Putnins P5', function () {

    var s_fwd_expect = [
       [ 226367.21338056153,  113204.56855847509],
       [ 226367.21338056153, -113204.56855847509],
       [-226367.21338056153,  113204.56855847509],
       [-226367.21338056153, -113204.56855847509]
    ];
    var s_inv_expect = [
       [ 0.00176671315102969553,  0.000883356575387199546],
       [ 0.00176671315102969553, -0.000883356575387199546],
       [-0.00176671315102969553,  0.000883356575387199546],
       [-0.00176671315102969553, -0.000883356575387199546]
    ];
    var sargs = "+proj=putp5   +a=6400000    +lat_1=0 +lat_2=2";
    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })

  describe('Putnins P5\'', function () {

    var s_fwd_expect = [
       [ 226388.175248755841,  113204.56855847509],
       [ 226388.175248755841, -113204.56855847509],
       [-226388.175248755841,  113204.56855847509],
       [-226388.175248755841, -113204.56855847509]
    ];
    var s_inv_expect = [
       [ 0.00176671315090204742,  0.000883356575387199546],
       [ 0.00176671315090204742, -0.000883356575387199546],
       [-0.00176671315090204742,  0.000883356575387199546],
       [-0.00176671315090204742, -0.000883356575387199546]
    ];
    var sargs = "+proj=putp5p   +a=6400000    +lat_1=0 +lat_2=2";
    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })
});
