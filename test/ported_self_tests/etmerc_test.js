var assert = require('assert'),
    api = require('../../'),
    helpers = require('../helpers');

describe('etmerc.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
    [ 200, 100],
    [ 200,-100],
    [-200, 100],
    [-200,-100]
  ];

  describe('Extended Transverse Mercator', function () {

    var fwd_expect = [
    [222650.79679758562,  110642.22941193319],
    [222650.79679758562,  -110642.22941193319],
    [-222650.79679758562,  110642.22941193319],
    [-222650.79679758562,  -110642.22941193319]
    ];

    var inv_expect = [
    [0.0017966305681649398,  0.00090436947663183873],
    [0.0017966305681649398,  -0.00090436947663183873],
    [-0.0017966305681649398,  0.00090436947663183873],
    [-0.0017966305681649398,  -0.00090436947663183873]
    ];

    var args = "+proj=etmerc   +ellps=GRS80  +lat_1=0.5 +lat_2=2 +n=0.5 +zone=30";

    helpers.fwd_test(args, fwd_in, fwd_expect);
    helpers.inv_test(args, inv_in, inv_expect);
  })


  describe('UTM', function () {

    var fwd_expect = [
    [1057002.4054912981,  110955.14117594929],
    [1057002.4054912981,  -110955.14117594929],
    [611263.81227890507,  110547.10569680421],
    [611263.81227890507,  -110547.10569680421]
    ];

    var inv_expect = [
    [-7.4869520833902357,  0.00090193980983462605],
    [-7.4869520833902357,  -0.00090193980983462605],
    [-7.4905356820622613,  0.00090193535121489081],
    [-7.4905356820622613,  -0.00090193535121489081]
    ];

    var args = "+proj=utm   +ellps=GRS80  +lat_1=0.5 +lat_2=2 +n=0.5 +zone=30";

    helpers.fwd_test(args, fwd_in, fwd_expect);
    helpers.inv_test(args, inv_in, inv_expect);
  })

});
