var assert = require('assert'),
    api = require('../'),
    helpers = require('./helpers');

describe('sterea.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
    [ 200, 100],
    [ 200,-100],
    [-200, 100],
    [-200,-100]
  ];

  describe('Oblique Stereographic Alternative', function () {

    var s_fwd_expect = [
    [ 223407.81025950745,  111737.93899644315],
    [ 223407.81025950745, -111737.93899644315],
    [-223407.81025950745,  111737.93899644315],
    [-223407.81025950745, -111737.93899644315]
    ];

    var s_inv_expect = [
    [ 0.001790493109747395,  0.00089524655465446378],
    [ 0.001790493109747395, -0.00089524655465446378],
    [-0.001790493109747395,  0.00089524655465446378],
    [-0.001790493109747395, -0.00089524655465446378]
    ];

    var e_fwd_expect = [
    [ 222644.89410919772,  110611.09187173686],
    [ 222644.89410919772, -110611.09187173827],
    [-222644.89410919772,  110611.09187173686],
    [-222644.89410919772, -110611.09187173827]
    ];

    var e_inv_expect = [
    [ 0.0017966305682019911,  0.00090436947683099009],
    [ 0.0017966305682019911, -0.00090436947684371233],
    [-0.0017966305682019911,  0.00090436947683099009],
    [-0.0017966305682019911, -0.00090436947684371233]
    ];

    var sargs = "+proj=sterea   +a=6400000    +lat_1=0.5 +lat_2=2 +n=0.5";
    var eargs = "+proj=sterea   +ellps=GRS80  +lat_1=0.5 +lat_2=2 +n=0.5";

    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
    helpers.fwd_test(eargs, fwd_in, e_fwd_expect);
    helpers.inv_test(eargs, inv_in, e_inv_expect);
  })

});
