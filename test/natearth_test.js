var assert = require('assert'),
    api = require('../'),
    helpers = require('./helpers');

describe('natearth.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
    [ 200, 100],
    [ 200,-100],
    [-200, 100],
    [-200,-100]
  ];

  describe('Natural Earth', function () {

    var s_fwd_expect = [
    [ 194507.265257889288,  112508.737358294515],
    [ 194507.265257889288, -112508.737358294515],
    [-194507.265257889288,  112508.737358294515],
    [-194507.265257889288, -112508.737358294515]
    ];

    var s_inv_expect = [
    [ 0.00205638349586440223,  0.000888823913291242177],
    [ 0.00205638349586440223, -0.000888823913291242177],
    [-0.00205638349586440223,  0.000888823913291242177],
    [-0.00205638349586440223, -0.000888823913291242177]
    ];

    var sargs = "+proj=natearth   +a=6400000    +lat_1=0.5 +lat_2=2";

    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })

  describe('Natural Earth 2', function () {

    var s_fwd_expect = [
    [ 189255.172934730799,  113022.495810907014],
    [ 189255.172934730799, -113022.495810907014],
    [-189255.172934730799,  113022.495810907014],
    [-189255.172934730799, -113022.495810907014]
    ];

    var s_inv_expect = [
    [ 0.00211344929691056112,  0.000884779612080993237],
    [ 0.00211344929691056112, -0.000884779612080993237],
    [-0.00211344929691056112,  0.000884779612080993237],
    [-0.00211344929691056112, -0.000884779612080993237]
    ];

    var sargs = "+proj=natearth2   +a=6400000    +lat_1=0.5 +lat_2=2";

    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })
});
