var assert = require('assert'),
    api = require('../'),
    helpers = require('./helpers');

describe('stere.js', function () {

  var fwd_in = [
    [ 2, 1],
    [ 2,-1],
    [-2, 1],
    [-2,-1]
  ];

  var inv_in = [
    [ 200, 100],
    [ 200,-100],
    [-200, 100],
    [-200,-100]
  ];
  describe('Stereographic', function () {
    var e_fwd_expect = [
      [ 445289.70910023432,  221221.76694834774],
      [ 445289.70910023432, -221221.76694835056],
      [-445289.70910023432,  221221.76694834774],
      [-445289.70910023432, -221221.76694835056]
    ];

    var s_fwd_expect = [
      [ 223407.81025950745,  111737.938996443],
      [ 223407.81025950745, -111737.938996443],
      [-223407.81025950745,  111737.938996443],
      [-223407.81025950745, -111737.938996443]
    ];

    var e_inv_expect = [
      [ 0.0017966305682022392,  0.00090436947502443507],
      [ 0.0017966305682022392, -0.00090436947502443507],
      [-0.0017966305682022392,  0.00090436947502443507],
      [-0.0017966305682022392, -0.00090436947502443507]
    ];

    var s_inv_expect = [
      [ 0.001790493109747395,  0.00089524655465513144],
      [ 0.001790493109747395, -0.00089524655465513144],
      [-0.001790493109747395,  0.00089524655465513144],
      [-0.001790493109747395, -0.00089524655465513144]
    ];

    var eargs = "+proj=stere   +ellps=GRS80  +lat_1=0.5 +lat_2=2 +n=0.5";
    var sargs = "+proj=stere   +a=6400000    +lat_1=0.5 +lat_2=2 +n=0.5";

    helpers.fwd_test(eargs, fwd_in, e_fwd_expect);
    helpers.fwd_test(sargs, fwd_in, s_fwd_expect);
    helpers.inv_test(eargs, inv_in, e_inv_expect);
    helpers.inv_test(sargs, inv_in, s_inv_expect);
  })


});
